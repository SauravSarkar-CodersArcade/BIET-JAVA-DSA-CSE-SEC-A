package com.biet.basics;
public class TernaryOperator {
    public static void main(String[] args) {
        String val1 = (3 < 4) ? "Yes" : "No";
        int val2 = (3 > 4) ? 1 : 0;
        boolean val3 = (3 == 4) ? true : false;
        System.out.println(val1 + "," + val2 + ","
        + val3);
    }
}
